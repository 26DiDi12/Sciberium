document.getElementById('product_ninja_title_num1').addEventListener('click', function () {
    if (document.getElementById('product_ninja_title_num1').classList[1] != 'active') {
        document.getElementById('product_ninja_title_num1').classList.add('active');
        document.getElementById('product_ninja_num1').classList.add('active');
        document.getElementById('product_ninja_text_num1').classList.add('active');
    } else {
        document.getElementById('product_ninja_title_num1').classList.remove('active');
        document.getElementById('product_ninja_num1').classList.remove('active');
        document.getElementById('product_ninja_text_num1').classList.remove('active');
    }
})
